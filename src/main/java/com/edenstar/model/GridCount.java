package com.edenstar.model;

public class GridCount {
	
	private int grid_max;

	public GridCount(int grid_max) {
		super();
		this.grid_max = grid_max;
	}

	public GridCount() {
		super();
	}

	public int getGrid_max() {
		return grid_max;
	}

	public void setGrid_max(int grid_max) {
		this.grid_max = grid_max;
	}
	
	

}
